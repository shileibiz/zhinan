class EmploymentCollector(BaseCollector):
    """就业率数据采集器。

    基于麦可思《中国本科生就业报告》（就业蓝皮书）公开摘要数据，
    内建 2023-2025 年全国本科就业率、专业就业率、红牌/绿牌专业、平均薪资。

    后期扩展方向：
    1. 逐校《就业质量年度报告》PDF 解析 → 学校级就业数据
    2. 教育部统计公报自动采集 → 全国宏观就业率
    3. 行业薪资数据库 → industry_stats 表
    """

    name = "employment"

    def __init__(self, **kwargs):
        kwargs.setdefault("delay", 1.0)
        super().__init__(**kwargs)
        self._major_name_map: dict[str, int] = {}

    async def collect(self) -> int:
        """执行就业数据采集。

        流程：
        1. 加载专业名称 → ID 映射
        2. 写入全国宏观就业率（作为 major_id=NULL 的聚合行 — 但由于
           employment_stats 要求 major_id NOT NULL，我们跳过此项）
        3. 为每个匹配的专业写入内建就业数据（2023/2024/2025）

        Returns:
            写入的记录总数
        """
        if not self.db:
            logger.error("No database backend set")
            return 0

        logger.info("EmploymentCollector started")
        total = 0

        # Step 1: 加载专业 ID 映射
        await self._load_major_maps()
        logger.info("Loaded %d major mappings", len(self._major_name_map))

        # Step 2: 写入专业级就业数据
        count = await self._import_employment_data()
        total += count
        logger.info("Imported %d employment stat records", count)

        logger.info(
            "EmploymentCollector done: %d records in employment_stats. "
            "Data sourced from MyCOS 麦可思《中国本科生就业报告》public summary.",
            total,
        )
        return total

    async def _load_major_maps(self):
        """加载专业名称 → ID 映射。"""
        rows = await self.db.fetch_all("SELECT id, name FROM majors")
        for row in rows:
            self._major_name_map[row["name"]] = row["id"]

    def _find_major_id(self, name: str) -> Optional[int]:
        """通过专业名称（精确或模糊）查找专业 ID。

        Args:
            name: 专业名称

        Returns:
            匹配的专业 ID，或 None
        """
        # 精确匹配
        mid = self._major_name_map.get(name)
        if mid:
            return mid

        # 模糊匹配：查找包含关系
        for db_name, db_id in self._major_name_map.items():
            if name in db_name or db_name in name:
                return db_id

        return None

    async def _import_employment_data(self) -> int:
        """导入内建专业就业数据到 employment_stats 表。

        Returns:
            写入的记录数
        """
        count = 0
        skipped = 0

        for year, majors_data in MYKOS_EMPLOYMENT_STATS.items():
            for major_name, (
                employed_pct, avg_salary, grad_count, industry_json, tag
            ) in majors_data.items():
                major_id = self._find_major_id(major_name)
                if not major_id:
                    skipped += 1
                    logger.debug("Major not found: %s, skipping", major_name)
                    continue

                # 检查是否已存在（避免重复写入同一专业同一年份）
                existing = await self.db.fetch_one(
                    "SELECT id FROM employment_stats WHERE major_id = ? AND year = ?",
                    (major_id, year),
                )
                if existing:
                    logger.debug(
                        "Employment data already exists for major '%s' (%d), skipping",
                        major_name, year,
                    )
                    continue

                await self.db.execute(
                    """INSERT INTO employment_stats
                       (major_id, year, grad_count, employed_pct, industry_distribution, avg_start_salary)
                       VALUES (?, ?, ?, ?, ?, ?)""",
                    (
                        major_id,
                        year,
                        grad_count,
                        employed_pct,
                        industry_json,
                        avg_salary,
                    ),
                )
                count += 1

        if skipped:
            logger.info(
                "Skipped %d major entries (not found in majors table). "
                "These majors may need to be added to the majors directory.",
                skipped,
            )

        return count
