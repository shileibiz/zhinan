def import_campuses(conn, campus_data):
    """导入校区信息。"""
    school_map = get_school_map(conn)
    cur = conn.cursor()
    count = 0
    skipped = 0

    for school_name, campuses in campus_data.items():
        sid = fuzzy_match_school(school_name, school_map)
        if not sid:
            skipped += 1
            logger.warning(f"Campus school not found: {school_name}")
            continue

        for name, address, function in campuses:
            cur.execute(
                "SELECT id FROM campuses WHERE school_id=? AND name=?",
                (sid, name),
            )
            if not cur.fetchone():
                cur.execute(
                    """INSERT INTO campuses
                       (school_id, name, address, function)
                       VALUES (?, ?, ?, ?)""",
                    (sid, name, address, function),
                )
                count += 1

    conn.commit()
    logger.info(f"Imported {count} campus records ({skipped} schools skipped)")
    return count


def import_enrollment(conn, enrollment_data, year=2024):
    """导入招生计划总数（作为学校总人数）。"""
    school_map = get_school_map(conn)
    cur = conn.cursor()
    count = 0
    skipped = 0

    for school_name, total in enrollment_data.items():
        sid = fuzzy_match_school(school_name, school_map)
        if not sid:
            skipped += 1
            logger.warning(f"Enrollment school not found: {school_name}")
            continue

        # 更新学校总招生人数
        cur.execute(
            "UPDATE schools SET total_enrollment = ? WHERE id = ?",
            (total, sid),
        )
        if cur.rowcount:
            count += 1

    conn.commit()
    logger.info(f"Updated {count} school enrollment totals ({skipped} skipped)")
    return count


