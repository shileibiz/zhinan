def get_db_connection():
    """获取数据库连接。"""
    conn = sqlite3.connect(DB_PATH)
    conn.row_factory = sqlite3.Row
    return conn


def get_school_map(conn):
    """获取学校名称到ID的映射。"""
    cur = conn.cursor()
    cur.execute("SELECT id, name FROM schools")
    mapping = {}
    for row in cur.fetchall():
        name = row["name"].strip()
        mapping[name] = row["id"]
        # 别名
        clean = re.sub(r"[（(][^）)]*[）)]", "", name).strip()
        if clean != name and clean:
            mapping[clean] = row["id"]
    return mapping


def fuzzy_match_school(name, school_map):
    """模糊匹配学校名称。"""
    if name in school_map:
        return school_map[name]

    # 去括号
    clean = re.sub(r"\s+", "", name)
    clean = re.sub(r"[（(][^）)]*[）)]", "", clean)
    for db_name, sid in school_map.items():
        db_clean = re.sub(r"\s+", "", db_name)
        db_clean = re.sub(r"[（(][^）)]*[）)]", "", db_clean)
        if clean == db_clean:
            return sid

    # 包含匹配
    name_clean = name.replace("（", "(").replace("）", ")")
    for db_name, sid in school_map.items():
        if name_clean in db_name or db_name in name_clean:
            return sid
        # 去掉空格匹配
        if re.sub(r"\s+", "", name_clean) == re.sub(r"\s+", "", db_name):
            return sid

    return None


def import_scores(conn, scores_list, year, province="广东"):
    """导入录取分数数据。"""
    school_map = get_school_map(conn)
    cur = conn.cursor()
    count = 0
    skipped = 0

    for row in scores_list:
        school_name, physics_score, physics_rank, history_score, history_rank = row

        sid = fuzzy_match_school(school_name, school_map)
        if not sid:
            skipped += 1
            logger.debug(f"School not found: {school_name} — skipping")
            continue

        # 物理类
        if physics_score is not None:
            batch = "本科批_物理"
            # 检查是否已存在（去重）
            cur.execute(
                "SELECT id FROM admission_scores WHERE school_id=? AND year=? AND province=? AND batch=? AND major_id IS NULL",
                (sid, year, province, batch),
            )
            if not cur.fetchone():
                cur.execute(
                    """INSERT INTO admission_scores
                       (school_id, major_id, year, province, batch,
                        max_score, min_score, avg_score, min_rank)
                       VALUES (?, NULL, ?, ?, ?, NULL, ?, NULL, ?)""",
                    (sid, year, province, batch, float(physics_score), physics_rank),
                )
                count += 1

        # 历史类
        if history_score is not None:
            batch = "本科批_历史"
            cur.execute(
                "SELECT id FROM admission_scores WHERE school_id=? AND year=? AND province=? AND batch=? AND major_id IS NULL",
                (sid, year, province, batch),
            )
            if not cur.fetchone():
                cur.execute(
                    """INSERT INTO admission_scores
                       (school_id, major_id, year, province, batch,
                        max_score, min_score, avg_score, min_rank)
                       VALUES (?, NULL, ?, ?, ?, NULL, ?, NULL, ?)""",
                    (sid, year, province, batch, float(history_score), history_rank),
                )
                count += 1

    conn.commit()
    logger.info(f"Imported {count} score records for {province} {year} ({skipped} schools not found)")
    return count


